<script>
export default {
  data() {
    return {
      news: {},
    }
  },
  mounted() {
    this.$nextTick(() => {
      this.$nuxt.$loading.start()
    })
    this.getNews()
  },
  methods: {
    getNews() {
      const params = {
        include: 'author',
      }
      this.$store
        .dispatch('news/get', { id: this.$route.params.id, params })
        .then((news) => {
          const { results } = news
          this.$nextTick(() => {
            this.$nuxt.$loading.finish()
          })
          this.news = results
        })
    },
  },
}
